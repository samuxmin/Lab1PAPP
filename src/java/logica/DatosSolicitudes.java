public class DatosSolicitudes {
    private String accion;
    private String correoSeguidor;
    private String correoSeguido;

    // Getters y setters para los campos

    public String getAccion() {
        return accion;
    }

    public void setAccion(String accion) {
        this.accion = accion;
    }

    public String getCorreoSeguidor() {
        return correoSeguidor;
    }

    public void setCorreoSeguidor(String correoSeguidor) {
        this.correoSeguidor = correoSeguidor;
    }

    public String getCorreoSeguido() {
        return correoSeguido;
    }

    public void setCorreoSeguido(String correoSeguido) {
        this.correoSeguido = correoSeguido;
    }
}

